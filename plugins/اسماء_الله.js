let handler = async (m, { args, usedPrefix, command }) => {
    if (!args[0]) {
        let data = global.asmaulhusna.map(a => `${a.index}. ${a.arabic}\n${a.translation_id}`).join('\n\n');
        return m.reply(`${contoh}\n\n${data}\n\n${anjuran}`);
    }

    let nomor = parseInt(args[0]);
    if (isNaN(nomor) || nomor < 1 || nomor > 99) return m.reply(`مثال:\n${usedPrefix + command} 1`);

    let { index, arabic, translation_id } = global.asmaulhusna[nomor - 1];
    return m.reply(`الله ${index}\n${arabic}\n${translation_id}`);
};

